import re


def main():
    print(validate(input("IPv4 Address: ")))


def validate(ip):
    """
    Regex to match numbers 0-255:
    - 250-255: 25[0-5]
    - 200-249: 2[0-4][0-9]
    - 100-199: 1[0-9]{2}
    - 0-99: [1-9]?[0-9]
    """
    return (
        True
        if re.search(
            r"^(25[0-5]|2[0-4][0-9]|1[0-9]{2}|[1-9]?[0-9])\."
            r"(25[0-5]|2[0-4][0-9]|1[0-9]{2}|[1-9]?[0-9])\."
            r"(25[0-5]|2[0-4][0-9]|1[0-9]{2}|[1-9]?[0-9])\."
            r"(25[0-5]|2[0-4][0-9]|1[0-9]{2}|[1-9]?[0-9])$",
            ip,
        )
        else False
    )


if __name__ == "__main__":
    main()
